# a, b, c
a = 5
b = -3
c = 7


summa = 0

if a > 0:  # a = 5. 5 > 0 - True
    print("А он сан.")
    summa = summa + a  # summa = 0 + 5 = 5

if b > 0:  # b = -3. -3 > 0 - False
    print("B он сан.")
    summa = summa + b

if c > 0:  # c = 7. 7 > 0 - True
    print("C он сан.")
    summa = summa + c  # summa = 5 + 7 = 12

print("summa:", summa)



















